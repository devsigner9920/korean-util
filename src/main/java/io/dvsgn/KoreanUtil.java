package io.dvsgn;

public class KoreanUtil {
    private final char[] KOREAN_INITIAL = {'ㄱ', 'ㄲ', 'ㄴ', 'ㄷ', 'ㄸ', 'ㄹ', 'ㅁ', 'ㅂ', 'ㅃ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅉ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ'};
    private final char[] KOREAN_NEUTRAL = {'ㅏ', 'ㅐ', 'ㅑ', 'ㅒ', 'ㅓ', 'ㅔ', 'ㅕ', 'ㅖ', 'ㅗ', 'ㅘ', 'ㅙ', 'ㅚ', 'ㅛ', 'ㅜ', 'ㅝ', 'ㅞ', 'ㅟ', 'ㅠ', 'ㅡ', 'ㅢ', 'ㅣ'};
    private final char[] KOREAN_FINAL = {'ㄱ', 'ㄲ', 'ㄳ', 'ㄴ', 'ㄵ', 'ㄶ', 'ㄷ', 'ㄹ', 'ㄺ', 'ㄻ', 'ㄼ', 'ㄽ', 'ㄾ', 'ㄿ', 'ㅀ', 'ㅁ', 'ㅂ', 'ㅄ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ'};
    private final int FIRST_KOREAN = '가';

    private boolean hasFinal(final char koreanChar) {
        return calculateKoreanFinalSequence(koreanChar) > 0;
    }

    private int calculateKoreanFinalSequence(final char koreanChar) {
        return calculateKoreanCharUnicodeSequence(koreanChar)
                % (KOREAN_NEUTRAL.length * KOREAN_FINAL.length)
                % KOREAN_FINAL.length;
    }

    private int calculateKoreanCharUnicodeSequence(final char koreanChar) {
        return koreanChar - FIRST_KOREAN;
    }
}
